import { createBrowserRouter } from "react-router-dom";
import { LoginPage, RegisterPage, LandingPage } from "./pages";
const router = createBrowserRouter([
  {
    path: "/",
    element: <LandingPage />,
  },
  {
    path: "/login",
    element: <LoginPage />,
  },
  {
    path: "/register",
    element: <RegisterPage />,
  },
]);

export default router;
